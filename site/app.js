/* VitaSort frontend — vanilla SPA, no build step.
   Loads data/products.json (generated by the pipeline) and renders
   home / results / product / saved views per the design file. */
(function () {
  'use strict';

  const state = {
    view: 'home', category: null, search: '', sortKey: 'value', sortDir: 'asc',
    filterVal: '', toggles: {}, stock: false, productId: null, saved: [], limit: 60,
  };
  let DATA = null; // { generated, categories: {key:cfg}, products: [...] }

  // ---------- persistence ----------
  try {
    const s = JSON.parse(localStorage.getItem('vitasort_saved') || '[]');
    if (Array.isArray(s)) state.saved = s;
  } catch (e) {}
  function persist() { try { localStorage.setItem('vitasort_saved', JSON.stringify(state.saved)); } catch (e) {} }

  // ---------- helpers ----------
  const esc = (s) => String(s == null ? '' : s).replace(/[&<>"']/g, c => ({'&':'&amp;','<':'&lt;','>':'&gt;','"':'&quot;',"'":'&#39;'}[c]));
  const cat = () => DATA.categories[state.category] || Object.values(DATA.categories)[0];
  const catKeys = () => Object.keys(DATA.categories);
  const scoreColor = pct => {                       // 0-100 -> red -> amber -> green
    const h = pct <= 50 ? 4 + (pct / 50) * 38 : 42 + ((pct - 50) / 50) * 110;
    return `hsl(${Math.round(h)}, 60%, 36%)`;
  };
  const fmtPrice = v => '$' + v.toFixed(2).replace(/\.00$/, '');
  const fmtServ = v => '$' + v.toFixed(2);
  const fmtValue = (c, v) => '$' + v.toFixed(c.valueDecimals);
  const byId = id => DATA.products.find(p => p.id === id);
  const saved = id => state.saved.includes(id);

  function toggleSave(id) {
    state.saved = saved(id) ? state.saved.filter(x => x !== id) : [...state.saved, id];
    persist(); render();
  }
  function setSort(key) {
    if (state.sortKey === key) state.sortDir = state.sortDir === 'asc' ? 'desc' : 'asc';
    else { state.sortKey = key; state.sortDir = key === 'score' ? 'desc' : 'asc'; }
    render();
  }
  function openCategory(k) {
    Object.assign(state, { view: 'results', category: k, filterVal: '', toggles: {}, search: '', limit: 60 });
    render(); window.scrollTo(0, 0);
  }
  function go(view) { state.view = view; render(); window.scrollTo(0, 0); }

  // ---------- row templates ----------
  function miniHTML(p) {
    return p.image
      ? `<img class="mini" src="${esc(p.image)}" alt="" loading="lazy" onerror="this.replaceWith(Object.assign(document.createElement('span'),{className:'mini'}))">`
      : '<span class="mini"></span>';
  }
  function ringHTML(p, small) {
    const size = small ? 'width:30px;height:30px;font-size:11px;margin:0 auto;' : '';
    const c = scoreColor(p.score);
    return `<span class="ring" style="${size}border-color:${c};color:${c}">${p.score}</span>`;
  }
  function trowHTML(p, showCat, heart) {
    const c = DATA.categories[p.cat];
    const sub = showCat ? `${esc(c.label)} · ${esc(p.brand)}` : esc(p.brand);
    return `<div class="trow" data-open="${p.id}">
      ${miniHTML(p)}
      <div><div class="tname">${esc(p.name)}</div><div class="tbrand">${sub}</div></div>
      <div class="tval"><div class="v">${fmtValue(c, p.valuePer)}</div><div class="u">${esc(c.valueUnit)}</div></div>
      ${ringHTML(p)}${heart ? `<span class="heart on" data-save="${p.id}">♥</span>` : ''}
    </div>`;
  }

  // ---------- views ----------
  function homeHTML() {
    const top = [...DATA.products].sort((a, b) => b.valueScore - a.valueScore).slice(0, 3);
    return `
    <div class="hero">
      <h1 class="h1">Find the best value supplement.</h1>
      <p class="hsub">Ranked by price per gram of active ingredient — no filler math.</p>
      <div class="search"><span class="mag"></span>
        <input id="homeSearch" placeholder="Search protein, creatine, pre-workout…" value="${esc(state.search)}"></div>
      <div class="chips">${catKeys().map(k =>
        `<span class="chip" data-cat="${k}">${esc(DATA.categories[k].label)}</span>`).join('')}</div>
    </div>
    <div class="htop">
      <div class="htop-h"><span class="eyebrow">Best value right now</span><span class="eyebrow">Score · $/unit</span></div>
      <div class="tlist">${top.map(p => trowHTML(p)).join('')}</div>
    </div>`;
  }

  function resultsHTML() {
    const c = cat();
    let list = DATA.products.filter(p => p.cat === state.category);
    const q = state.search.trim().toLowerCase();
    if (q) list = list.filter(p => (p.name + ' ' + p.brand).toLowerCase().includes(q));
    if (state.filterVal && c.filter) list = list.filter(p => String(p.attrs[c.filter.field]) === state.filterVal);
    (c.toggles || []).forEach(t => { if (state.toggles[t.field]) list = list.filter(p => p.attrs[t.field]); });
    if (state.stock) list = list.filter(p => p.stock);
    const dir = state.sortDir === 'asc' ? 1 : -1;
    list.sort((a, b) => {
      const f = state.sortKey === 'price' ? 'price' : state.sortKey === 'score' ? 'score' : 'valuePer';
      return (a[f] - b[f]) * dir;
    });
    const arrow = k => state.sortKey === k ? (state.sortDir === 'asc' ? ' ↑' : ' ↓') : '';
    const sc = k => state.sortKey === k ? 'sorth act' : 'sorth';

    return `
    <div class="rtop">
      <div class="tabs">${catKeys().map(k =>
        `<span class="tab${k === state.category ? ' on' : ''}" data-tab="${k}">${esc(DATA.categories[k].label)}</span>`).join('')}</div>
      <div class="fbar">
        <div class="fsearch"><span class="mag" style="width:13px;height:13px"></span>
          <input id="fSearch" placeholder="Filter by name or brand" value="${esc(state.search)}"></div>
        ${c.filter ? `<select class="fsel" id="fSel">
          <option value="">${esc(c.filter.allLabel)}</option>
          ${c.filter.options.map(o => `<option value="${esc(o)}"${state.filterVal === o ? ' selected' : ''}>${esc(o)}</option>`).join('')}
        </select>` : ''}
        ${(c.toggles || []).map(t =>
          `<span class="ftog${state.toggles[t.field] ? ' on' : ''}" data-tog="${t.field}"><span class="dot"></span>${esc(t.label)}</span>`).join('')}
        <span class="ftog${state.stock ? ' on' : ''}" data-tog="__stock"><span class="dot"></span>In stock</span>
        <span class="fcount"><b>${list.length}</b> results</span>
      </div>
      <div class="rhead">
        <span>Product</span>${c.columns.map(h => `<span>${esc(h)}</span>`).join('')}
        <span class="tr"><span class="${sc('price')}" data-sort="price">Price${arrow('price')}</span></span>
        <span class="tr"><span class="${sc('value')}" data-sort="value">${esc(c.valueHeader)}${arrow('value')}</span></span>
        <span class="tc"><span class="${sc('score')}" data-sort="score">Score${arrow('score')}</span></span><span></span>
      </div>
      <div class="rlist">${list.slice(0, state.limit).map(p => `
        <div class="rrow" data-open="${p.id}">
          <div class="pcell">${miniHTML(p)}<div style="min-width:0">
            <div class="pn">${esc(p.name)}</div><div class="pb">${esc(p.brand)}</div></div></div>
          <span class="cval">${esc(p.cols[0])}</span><span class="cmut">${esc(p.cols[1])}</span><span class="cmut">${esc(p.cols[2])}</span>
          <span class="tr pprice">${fmtPrice(p.price)}</span><span class="tr pval">${fmtValue(c, p.valuePer)}</span>
          <span class="tc">${ringHTML(p, true)}</span>
          <span class="heart${saved(p.id) ? ' on' : ''}" data-save="${p.id}">♥</span>
        </div>`).join('') || '<div class="empty">No products match those filters.</div>'}
      ${list.length > state.limit ? '<div id="moreSentinel" class="loading" style="padding:18px">Loading more…</div>' : ''}</div>
    </div>`;
  }

  function productHTML() {
    const p = byId(state.productId);
    if (!p) return '<div class="empty">Product not found.</div>';
    const c = DATA.categories[p.cat];
    const isSaved = saved(p.id);
    const ratingPct = Math.round(p.stars / 5 * 100);
    const specs = p.specs.map(([k, v], i) =>
      `<div class="spec${i === p.specs.length - 1 ? ' hl' : ''}"><span class="k">${esc(k)}</span><span class="v">${esc(v)}</span></div>`).join('');
    return `
    <span class="pback" data-nav="results">← Back to results</span>
    <div class="pgrid">
      <div>
        ${p.image
          ? `<div class="pimg has"><img src="${esc(p.image)}" alt="${esc(p.name)}"></div>`
          : '<div class="pimg"><span>product shot</span></div>'}
        <div class="pbuy">
          <div class="bigprice">${fmtPrice(p.price)}</div>
          <div class="perserv">${fmtServ(p.price / p.servings)} per serving · ${p.servings} servings</div>
          ${p.url ? `<a class="btn buylink" href="${esc(p.url)}" target="_blank" rel="noopener nofollow">Buy at ${esc(p.retailer)} →</a>`
                  : `<button class="btn" disabled>Available at ${esc(p.retailer)}</button>`}
          <button class="btn sec${isSaved ? ' on' : ''}" data-save="${p.id}">${isSaved ? '♥ Saved' : '♡ Save'}</button>
          ${(p.offers && p.offers.length > 1) ? `<div class="offers">
            <div class="offers-h">All retailers</div>
            ${p.offers.map((o, i) => `<a class="offer${i === 0 && o.price != null ? ' best' : ''}"
              ${o.url ? `href="${esc(o.url)}" target="_blank" rel="noopener nofollow"` : ''}>
              <span class="o-r">${esc(o.retailer)}${i === 0 && o.price != null ? '<span class="o-tag">Best price</span>' : ''}${o.price != null && !o.in_stock ? '<span class="o-tag oos">Out of stock</span>' : ''}</span>
              <span class="o-p">${o.price != null ? '$' + o.price.toFixed(2) : 'See price →'}</span></a>`).join('')}
          </div>` : ''}
        </div>
      </div>
      <div>
        <div class="pcat">${esc(c.label)}</div>
        <h1 class="ptitle">${esc(p.name)}</h1>
        <div class="pbrand2">${esc(p.brand)} · <span class="stars">${p.stars.toFixed(1)}★ <span class="rev">${p.reviews.toLocaleString()} reviews</span></span></div>
        <div class="vcard">
          <div class="vhead"><span class="ring bigring" style="border-color:${scoreColor(p.score)};color:${scoreColor(p.score)}">${p.score}</span>
            <div><div class="vtitle">VitaScore</div><div class="vnote">Loved supplements, lowest price — one number.</div></div></div>
          <div class="bars">
            <div class="barrow"><span class="barlbl">Community rating</span>
              <span class="track"><span class="fill" style="width:${ratingPct}%;background:${scoreColor(ratingPct)}"></span></span>
              <span class="barval">${p.stars.toFixed(1)}★</span></div>
            <div class="barrow"><span class="barlbl">Price value</span>
              <span class="track"><span class="fill" style="width:${p.valueScore}%;background:${scoreColor(p.valueScore)}"></span></span>
              <span class="barval">${p.valueScore}/100</span></div>
          </div>
          <div class="vprev">VitaScore blends verified review ratings with cost per active gram, weighted 50/50.</div>
        </div>
        <div class="specs"><div class="specs-h">Supplement facts</div>${specs}</div>
      </div>
    </div>`;
  }

  function savedHTML() {
    const rows = DATA.products.filter(p => saved(p.id));
    return `
    <div class="svhead">Saved supplements</div>
    <div class="svsub">${rows.length} saved across all categories.</div>
    ${rows.length
      ? `<div class="tlist" style="padding-bottom:60px">${rows.map(p => trowHTML(p, true, true)).join('')}</div>`
      : '<div class="empty">Nothing saved yet. Tap the ♥ on any supplement to keep it here.</div>'}`;
  }

  // ---------- render + events ----------
  function render() {
    const el = document.getElementById('view');
    el.innerHTML = { home: homeHTML, results: resultsHTML, product: productHTML, saved: savedHTML }[state.view]();
    document.getElementById('savedBadge').textContent = state.saved.length;
    const sentinel = document.getElementById('moreSentinel');
    if (sentinel && 'IntersectionObserver' in window) {
      new IntersectionObserver((entries, obs) => {
        if (entries[0].isIntersecting) { obs.disconnect(); state.limit += 60; render(); }
      }, { rootMargin: '600px' }).observe(sentinel);
    }
  }

  document.addEventListener('click', (e) => {
    const t = e.target.closest('[data-save],[data-open],[data-nav],[data-cat],[data-tab],[data-tog],[data-sort]');
    if (!t) return;
    if (t.dataset.save) { e.stopPropagation(); toggleSave(t.dataset.save); return; }
    if (t.dataset.open) { state.productId = t.dataset.open; go('product'); return; }
    if (t.dataset.nav) { go(t.dataset.nav); return; }
    if (t.dataset.cat) { openCategory(t.dataset.cat); return; }
    if (t.dataset.tab) { Object.assign(state, { category: t.dataset.tab, filterVal: '', toggles: {}, limit: 60 }); render(); return; }
    if (t.dataset.tog) {
      if (t.dataset.tog === '__stock') state.stock = !state.stock;
      else state.toggles[t.dataset.tog] = !state.toggles[t.dataset.tog];
      render(); return;
    }
    if (t.dataset.sort) { setSort(t.dataset.sort); }
  });
  document.addEventListener('input', (e) => {
    if (e.target.id === 'homeSearch' || e.target.id === 'fSearch') {
      state.search = e.target.value;
      if (e.target.id === 'fSearch') {
        const pos = e.target.selectionStart; render();
        const el = document.getElementById('fSearch'); el.focus(); el.setSelectionRange(pos, pos);
      }
    }
  });
  document.addEventListener('keydown', (e) => {
    if (e.target.id === 'homeSearch' && e.key === 'Enter') openCategory(state.category || catKeys()[0]);
  });
  document.addEventListener('change', (e) => {
    if (e.target.id === 'fSel') { state.filterVal = e.target.value; render(); }
  });

  // ---------- boot ----------
  fetch('data/products.json')
    .then(r => { if (!r.ok) throw new Error(r.status); return r.json(); })
    .then(d => {
      DATA = d;
      state.category = Object.keys(d.categories)[0];
      const u = document.getElementById('updated');
      if (d.generated) u.textContent = 'Last updated ' + new Date(d.generated).toLocaleDateString();
      render();
    })
    .catch(() => {
      document.getElementById('view').innerHTML =
        '<div class="loaderr">Couldn\u2019t load the catalog. If you\u2019re running locally, serve this folder over HTTP (e.g. <code>python3 -m http.server</code>) and make sure the pipeline has generated <code>data/products.json</code>.</div>';
    });
})();
